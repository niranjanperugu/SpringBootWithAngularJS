package com.mytutor.model;

public class Parent {

	private int id;
	private String name;
	private String email;
	private String studentName;
	private String relation;
	private String studentQualification;
	private String intrestedSubjects;
	private String mobileNo;
	private String address;
	
	Parent(){
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public String getRelation() {
		return relation;
	}

	public void setRelation(String relation) {
		this.relation = relation;
	}

	public String getStudentQualification() {
		return studentQualification;
	}

	public void setStudentQualification(String studentQualification) {
		this.studentQualification = studentQualification;
	}

	public String getIntrestedSubjects() {
		return intrestedSubjects;
	}

	public void setIntrestedSubjects(String intrestedSubjects) {
		this.intrestedSubjects = intrestedSubjects;
	}

	public String getMobileNo() {
		return mobileNo;
	}

	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
